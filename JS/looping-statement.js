//Looping Statements - Excute repetitive task / way to automate task

//Loops: for loop
for (let i = 1; i <= 5; i++) {
    console.log("Count:", i);
}

//Loop: while loop
let count = 1;
while (count <= 5) {
    console.log("While Count:", count);
    count++;
}

//Loop: Do-while loop
let counter = 1;
do {
    console.log("Do while Count:", counter);
    counter++;
} while (counter <= 5);
